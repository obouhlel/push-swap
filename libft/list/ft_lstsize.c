#include "../libft.h"

//to get the list size
int	ft_lstsize(t_list *lst)
{
	int		size;

	size = 0;
	if (!lst)
		return (size);
	while (lst)
	{
		size++;
		lst = lst->next;
	}
	return (size);
}
